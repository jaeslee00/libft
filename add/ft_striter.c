#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;
	int len;

	i = 0;
	len = ft_strlen(s);
	if (s && f)
	{
		while (i < len && s[i] != '\0')
		{
			f(&s[i]);
			i++;
		}
		return ;
	}
	return ;
}
